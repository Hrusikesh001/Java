public class Logical_operators_04 {
    public static void main(String[] args) {
        // Demonstrating the logical AND operator (&&) if both conditions are true then the result is true
        System.out.println( (3>2) && (8<4));

        // Demonstrating the logical OR operator (||) if one of the conditions is true then the result is true
        System.out.println( (3>2) || (8<4));

        // Demonstrating the logical NOT operator (!) it reverses the result
        System.out.println( !(3>2) );
    }
}