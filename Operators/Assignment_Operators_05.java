public class Assignment_Operators_05 {
    public static void main(String[] args) {
        int A = 10;
        // A = A + 5; 
        A += 5; // Using the assignment operator to add 5 to A
        int B = 20;
        // B = B - 5;
        B -= 5; // Using the assignment operator to subtract 5 from B

        System.out.println("Value of A after A = A + 5: " + A);
        System.out.println("Value of B after B = B - 5: " + B);
        
    }
}
